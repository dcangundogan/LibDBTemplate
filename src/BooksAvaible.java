    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String query ="SELECT * FROM books;";
        DefaultTableModel table_model = (DefaultTableModel)jTable1.getModel();

        
    try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/libmanagementdb","root","bercan2003");
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(query);
            
           while (rs.next()){
               String isbn = rs.getString("ISBN");
               String title = rs.getString("Book-Title");
               String author = rs.getString("Book-Author");
               int year = rs.getInt("Year-of-Publication");
               int stock = rs.getInt("stock");
               table_model.addRow(new Object[]{isbn,title,author,year,stock});}

               rs.close();
               stm.close();

            }
            catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
 
        }         
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

