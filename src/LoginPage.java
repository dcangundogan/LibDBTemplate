    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String psswrd = new String(password.getPassword());
        String username = user.getText();
        String query = ("SELECT admin_password, admin_username FROM admin WHERE admin_username='"+username+"' ");

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/libmanagementdb","root","bercan2003");
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(query);
            
            if (rs.next()){
                String realpswrd = rs.getString("admin_password");
                if(realpswrd.equals(psswrd)){
                    AdminPanel adminPanel = new AdminPanel();
                    adminPanel.setVisible(true);
                    this.dispose();
                    JOptionPane.showMessageDialog(this, "Welcome to the system!");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Username or Password is wrong.");
                }
                
                
            }
            else{
                JOptionPane.showMessageDialog(this, "You don't have access");
            }
            
            
            
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

    
